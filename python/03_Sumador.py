#Muy bien ahora mostraremos la suma de dos numeros ingresados por el usuario
num1 = int(input("Ingresa el primer numero: "))
num2 = int(input("Ingresa el segundo numero: "))
suma = num1 + num2
print("La suma de", num1, "y", num2, "es:", suma)
#Con esto creamos el sumador basico que toma dos entradas del usuario, las convierte a enteros,
#Puedes cambiar int() por float() si deseas trabajar con numeros decimales
